        private TransaccionesEN InformacionDelaTransaccion(TasaDeCambioEN oTasaDeCambio, String TipoDeOperacion, String Descripcion, String Estado)
        {
            TransaccionesEN oRegistroEN = new TransaccionesEN();

            oRegistroEN.idregistro = oTasaDeCambio.idTasaDeCambio;
            oRegistroEN.Modelo = "TransaccionAD";
            oRegistroEN.Modulo = "Transacciones";
            oRegistroEN.Tabla = "TasaDeCambios";
            oRegistroEN.tipodeoperacion = TipoDeOperacion;
            oRegistroEN.Estado = Estado;
            oRegistroEN.ip = oTasaDeCambio.oLoginEN.NumeroIP;
            oRegistroEN.idusuario = oTasaDeCambio.oLoginEN.idUsuario;
            oRegistroEN.idusuarioaprueba = oTasaDeCambio.oLoginEN.idUsuario;
            oRegistroEN.descripciondelusuario = DescripcionDeOperacion;
            oRegistroEN.descripcioninterna = Descripcion;
            oRegistroEN.NombreDelEquipo = oTasaDeCambio.oLoginEN.NombreDelComputador;

            return oRegistroEN;
        }


        private string InformacionDelRegistro(TasaDeCambioEN oRegistroEN) {
            string Cadena = @"idTasaDeCambio: {0}, Fecha: {1}, Cambio: {2}";
            Cadena = string.Format(Cadena, oRegistroEN.idTasaDeCambio, oRegistroEN.Fecha.ToLongDateString(),string.Format("{0:###,##0.0000}",oRegistroEN.Cambio.ToString()));
            Cadena = Cadena.Replace(",", Environment.NewLine);
            return Cadena;            
        }

